a = int(input('Введите значение для определения диапазона '))
if 0 <= a <= 10:
    if a <= 3:
        print(f'Значение переменной равное {a} входит в диапазон от 0 до 3')
    elif 3 < a < 6:
        print(f'Значение переменной равное {a} входит в диапазон от 3 до 6')
    else:
        print(f'Значение переменной равное {a} входит в диапазон от 6 до 10')
else:
    print('Введенное значение не входит в диапазон от 0 до 10')
